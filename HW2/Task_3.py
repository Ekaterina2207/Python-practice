shop = [['каретка', 1200], ['шатун', 1000], ['седло', 300], ['педаль', 100],
        ['седло', 1500], ['рама', 12000], ['обод', 2000], ['шатун', 200],
        ['седло', 2700]]
total = 0
total_sum = 0
print('Введите название детали: ')
item_1 = input()
for item in shop:
    if item[0] == item_1:
        total += 1
        total_sum += item[1]
print('Кол-во деталей: ', total)
print('Общая стоимость: ', total_sum)
